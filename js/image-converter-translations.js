// Compatibility wrapper for older page code. The shared NegiI18n system is the
// single source of truth for applying translations.
window.ImageConverterTranslationSystem = {
    currentLang: 'ja',

    init: function() {
        this.currentLang = window.NegiI18n?.getLanguage?.() || localStorage.getItem('selectedLanguage') || 'ja';
        return this.currentLang;
    },

    switchLanguage: function(lang) {
        this.currentLang = lang === 'en' ? 'en' : 'ja';
        if (window.NegiI18n?.setLanguage) {
            window.NegiI18n.setLanguage(this.currentLang);
        } else {
            localStorage.setItem('selectedLanguage', this.currentLang);
            document.documentElement.lang = this.currentLang;
        }
    },

    applyTranslations: function() {
        if (window.NegiI18n?.apply) {
            window.NegiI18n.apply(this.currentLang);
        }
    }
};
