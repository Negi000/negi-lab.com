// Image Converter Tool Dedicated Translation System
window.ImageConverterTranslationSystem = {
    currentLang: 'ja',
    
    init: function() {
        console.log('ImageConverter TranslationSystem: Initializing...');
        this.detectLanguage();
        this.applyTranslations();
        this.bindLanguageSwitch();
    },
    
    detectLanguage: function() {
        const saved = localStorage.getItem('selectedLanguage');
        if (saved && (saved === 'ja' || saved === 'en')) {
            this.currentLang = saved;
        }
        
        const langSelect = document.getElementById('lang-switch');
        if (langSelect) {
            langSelect.value = this.currentLang;
        }
    },
    
    bindLanguageSwitch: function() {
        const langSelect = document.getElementById('lang-switch');
        if (langSelect) {
            langSelect.addEventListener('change', (e) => {
                this.switchLanguage(e.target.value);
            });
        }
    },
    
    switchLanguage: function(lang) {
        console.log('ImageConverter TranslationSystem: Switching to', lang);
        this.currentLang = lang;
        localStorage.setItem('selectedLanguage', lang);
        this.applyTranslations();
        
        // Debug: Detailed logging when switching languages
        console.log('=== ImageConverter Translation Debug ===');
        console.log('Current language:', this.currentLang);
        console.log('Available translations:', Object.keys(window.imageConverterTranslations || {}));
        console.log('Translation data sample:', window.imageConverterTranslations?.[this.currentLang] ? Object.keys(window.imageConverterTranslations[this.currentLang]).slice(0, 10) : 'None');
    },
    
    applyTranslations: function() {
        if (!window.imageConverterTranslations || !window.imageConverterTranslations[this.currentLang]) {
            console.error('ImageConverter translation data not available for', this.currentLang);
            return;
        }
        
        const translations = window.imageConverterTranslations[this.currentLang];
        const elements = document.querySelectorAll('[data-translate-key]');
        
        console.log('ImageConverter TranslationSystem: Found', elements.length, 'elements to translate');
        
        let translated = 0;
        elements.forEach(element => {
            const key = element.getAttribute('data-translate-key');
            
            // Remove imageConverter. prefix if present
            let cleanKey = key.replace(/^imageConverter\./, '');
            
            // Keep other prefixes for special keys
            if (key.startsWith('header.nav.') || key.startsWith('footer.') || 
                key.startsWith('disclaimer.') || key.startsWith('common.') || 
                key.startsWith('modal.')) {
                cleanKey = key;
            }
            
            const text = translations[cleanKey];
            
            if (text) {
                if (element.tagName === 'TITLE') {
                    element.textContent = text;
                    document.title = text;
                } else if (element.tagName === 'META') {
                    element.content = text;
                } else if (element.tagName === 'INPUT') {
                    element.placeholder = text;
                } else if (element.tagName === 'OPTION') {
                    element.textContent = text;
                } else if (element.tagName === 'OPTGROUP') {
                    element.label = text;
                } else {
                    // Processing for cases with HTML
                    if (text.includes('<br>') || text.includes('<small>') || text.includes('<strong>')) {
                        element.innerHTML = text;
                    } else {
                        element.textContent = text;
                    }
                }
                translated++;
            } else {
                console.warn('Translation not found for key:', cleanKey, 'original:', key);
            }
        });
        
        console.log('ImageConverter TranslationSystem: Translated', translated, 'elements');
        
        // Update page title and meta
        const pageTitle = translations['pageTitle'];
        if (pageTitle) {
            document.title = pageTitle;
        }
        
        const metaDesc = document.querySelector('meta[name="description"]');
        const metaDescription = translations['metaDescription'];
        if (metaDesc && metaDescription) {
            metaDesc.content = metaDescription;
        }
    }
};

