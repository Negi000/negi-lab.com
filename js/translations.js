// 現在の言語を取得・設定する関数
window.currentLanguage = 'ja';

window.getLanguage = function() {
  return window.currentLanguage || document.documentElement.lang || localStorage.getItem('selectedLanguage') || 'ja';
};

window.setLanguage = function(lang) {
  window.currentLanguage = lang;
  document.documentElement.lang = lang;
  localStorage.setItem('selectedLanguage', lang);
};

// 翻訳関数
window.t = function(key, fallback) {
  const lang = window.getLanguage();
  if (window.translations && window.translations[lang] && window.translations[lang][key]) {
    return window.translations[lang][key];
  }
  return fallback || key;
};

// 翻訳を適用する関数
window.applyTranslations = function(lang) {
  if (!lang) lang = window.getLanguage();
  
  window.setLanguage(lang);
    // data-translate-key属性を持つすべての要素を翻訳
  document.querySelectorAll('[data-translate-key]').forEach(function(element) {
    const key = element.getAttribute('data-translate-key');
    const translation = window.t(key);
    
    // title, metaタグ特殊処理
    if (element.tagName === 'TITLE') {
      element.textContent = translation;
    } else if (element.tagName === 'META') {
      element.setAttribute('content', translation);
    } else if (element.tagName === 'INPUT' && element.type === 'text' || element.type === 'number') {
      // input要素のplaceholder属性を更新
      element.placeholder = translation;
    } else {
      // HTMLコンテンツを含む翻訳（SEOセクションなどの場合）
      if (translation.includes('<strong>') || translation.includes('•')) {
        element.innerHTML = translation;
      } else {
        element.textContent = translation;
      }
    }
  });
  
  // 言語選択ボックスの更新
  const langSelect = document.getElementById('lang-switch');
  if (langSelect) {
    langSelect.value = lang;
  }
  
  // 単位変換ツール特有の翻訳処理
  if (typeof updateUnitLabels === 'function') {
    updateUnitLabels();
  }
};

/**
 * 翻訳システムクラス
 */
window.TranslationSystem = {
    currentLang: 'ja',
    
    // 初期化
    init: function() {
        this.detectLanguage();
        this.bindEvents();
        this.applyTranslations();
    },
    
    // 言語検出
    detectLanguage: function() {
        const saved = localStorage.getItem('selectedLanguage') || localStorage.getItem('preferred-language');
        const browser = navigator.language || navigator.userLanguage;
        
        if (saved && (saved === 'ja' || saved === 'en')) {
            this.currentLang = saved;
        } else if (browser.startsWith('en')) {
            this.currentLang = 'en';
        } else {
            this.currentLang = 'ja';
        }
        
        // 言語セレクターの状態更新
        const langSwitch = document.getElementById('lang-switch');
        if (langSwitch) {
            langSwitch.value = this.currentLang;
        }
    },
    
    // イベント設定
    bindEvents: function() {
        const langSwitch = document.getElementById('lang-switch');
        if (langSwitch) {
            langSwitch.addEventListener('change', (e) => {
                this.switchLanguage(e.target.value);
            });
        }
    },
    
    // 言語切り替え
    switchLanguage: function(lang) {
        if (lang !== 'ja' && lang !== 'en') return;
        
        this.currentLang = lang;
        localStorage.setItem('selectedLanguage', lang);
        localStorage.setItem('preferred-language', lang);
        this.applyTranslations();
        
        // HTMLのlang属性も更新
        document.documentElement.lang = lang;
    },
    
    // 翻訳適用
    applyTranslations: function() {
        const translations = window.translations[this.currentLang];
        if (!translations) return;
        
        // data-translate-key属性を持つ要素を翻訳
        document.querySelectorAll('[data-translate-key]').forEach(element => {
            const key = element.getAttribute('data-translate-key');
            const text = translations[key];
            
            if (text) {
                if (element.tagName === 'INPUT' && (element.type === 'text' || element.type === 'number')) {
                    element.placeholder = text;
                } else if (element.tagName === 'TITLE') {
                    element.textContent = text;
                } else if (element.tagName === 'META') {
                    element.content = text;
                } else if (element.tagName === 'OPTION') {
                    element.textContent = text;
                } else if (element.tagName === 'OPTGROUP') {
                    element.label = text;
                } else {
                    // HTMLタグが含まれている場合はinnerHTMLを使用
                    if (text.includes('<') || text.includes('&')) {
                        element.innerHTML = text;
                    } else {
                        element.textContent = text;
                    }
                }
            }
        });
        
        // タイトルとメタ情報の更新
        if (translations['imageConverter.pageTitle']) {
            document.title = translations['imageConverter.pageTitle'];
            
            const metaDesc = document.querySelector('meta[name="description"]');
            if (metaDesc) {
                metaDesc.content = translations['imageConverter.metaDescription'];
            }
        }
    },
    
    // 翻訳テキスト取得
    t: function(key, fallback = '') {
        const text = window.translations[this.currentLang][key];
        return text || fallback;
    }
};

// 後方互換性のため既存の関数も維持
window.applyTranslations = function(lang) {
    if (window.TranslationSystem) {
        window.TranslationSystem.switchLanguage(lang);
    }
};

// 初期化
document.addEventListener('DOMContentLoaded', function() {
  const savedLang = localStorage.getItem('selectedLanguage') || 'ja';
  
  // 新しい翻訳システムを優先
  if (window.TranslationSystem) {
    window.TranslationSystem.init();
  } else {
    // フォールバック
    window.applyTranslations(savedLang);
  }
  
  // 言語切り替えイベントリスナー
  const langSelect = document.getElementById('lang-switch');
  if (langSelect) {
    langSelect.addEventListener('change', function() {
      if (window.TranslationSystem) {
        window.TranslationSystem.switchLanguage(this.value);
      } else {
        window.applyTranslations(this.value);
      }
    });
  }
});
